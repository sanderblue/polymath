<?php

namespace Polymath\Geometry\Conversions;

class Conversions
{
    /**
    * Converts the radian number to the equivalent number in degrees
    */
    public function rad2deg($x) 
    {
      return rad2deg($x);
    }

    /**
    * Converts the radian number to the equivalent number in degrees
    */
    public function deg2rad($x) 
    {
      return deg2rad($x);
    }
}